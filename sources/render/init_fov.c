#include "../includes/cub3d.h"

void init_fov(t_geometry *geo)
{
    geo->x1 = 0;
    geo->x2 = 0;
    geo->x2 = 0; 
    geo->y2 = 0; 
    geo->dx = 0.0;
    geo->dy = 0.0;
}


